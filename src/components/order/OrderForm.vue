<template>
  <el-form
    ref="formRef"
    :model="form"
    :rules="rules"
    label-width="100px"
  >
    <el-form-item label="用户" prop="user_id">
      <el-select 
        v-model="form.user_id" 
        placeholder="请选择用户" 
        style="width: 100%"
        filterable
        remote
        :remote-method="remoteSearchUser"
        :loading="userLoading"
        clearable
      >
        <el-option
          v-for="user in userList"
          :key="user.id"
          :label="user.name"
          :value="user.id"
        />
      </el-select>
    </el-form-item>
    
    <el-form-item label="订单状态" prop="status">
      <el-select v-model="form.status" placeholder="请选择订单状态">
        <el-option label="待处理" value="pending" />
        <el-option label="处理中" value="processing" />
        <el-option label="已完成" value="completed" />
        <el-option label="已取消" value="cancelled" />
      </el-select>
    </el-form-item>

    <el-form-item label="订单商品" prop="items">
      <div class="order-items">
        <div v-for="(item, index) in form.items" :key="index" class="order-item">
          <el-select
            v-model="item.product_id"
            placeholder="选择商品"
            @change="handleProductChange($event, index)"
            style="width: 200px"
            filterable
            remote
            :remote-method="(query) => remoteSearchProduct(query, index)"
            :loading="productLoading[index]"
            clearable
          >
            <el-option
              v-for="product in productList"
              :key="product.id"
              :label="product.name"
              :value="product.id"
            />
          </el-select>
          <el-input-number
            v-model="item.quantity"
            :min="1"
            :precision="0"
            :step="1"
            style="width: 120px"
            @change="calculateTotal"
          />
          <span class="item-price" v-if="item.price">
            单价: ¥{{ item.price }}
          </span>
          <span class="item-subtotal" v-if="item.price && item.quantity">
            小计: ¥{{ (item.price * item.quantity).toFixed(2) }}
          </span>
          <el-button type="danger" link @click="removeItem(index)">
            删除
          </el-button>
        </div>
        <el-button type="primary" link @click="addItem">
          添加商品
        </el-button>
      </div>
    </el-form-item>

    <el-form-item label="订单总额">
      <span class="total-amount">¥{{ calculateTotalAmount() }}</span>
    </el-form-item>

    <el-form-item label="订单备注" prop="remark">
      <el-input
        v-model="form.remark"
        type="textarea"
        rows="3"
        placeholder="请输入订单备注"
      />
    </el-form-item>
  </el-form>
</template>

<script setup>
import { ref, watch, onMounted } from 'vue'
import { ElMessage } from 'element-plus'
import { createOrder, updateOrder } from '@/api/order'
import { getProductList } from '@/api/product'
import { getSimpleUserList } from '@/api/user'

const props = defineProps({
  formData: {
    type: Object,
    default: () => ({})
  }
})

const emit = defineEmits(['submit'])

const formRef = ref(null)
const productList = ref([])
const userList = ref([])
const form = ref({
  user_id: null,
  status: 'pending',
  items: [],
  remark: '',
  total_price: 0
})

// 添加加载状态和搜索相关变量
const userLoading = ref(false)
const productLoading = ref({})
const searchProductList = ref([]) // 用于存储搜索结果的商品列表

// 初始化一个新的商品项
const initOrderItem = () => ({
  product_id: '',
  quantity: 1,
  price: 0
})

// 添加商品项
const addItem = () => {
  form.value.items.push(initOrderItem())
  // 初始化新添加项的加载状态
  const newIndex = form.value.items.length - 1
  productLoading.value[newIndex] = false
}

// 移除商品项
const removeItem = (index) => {
  form.value.items.splice(index, 1)
  calculateTotal()
  // 清理对应的加载状态
  const newLoadingState = {}
  Object.keys(productLoading.value).forEach(key => {
    const numKey = parseInt(key)
    if (numKey < index) {
      newLoadingState[key] = productLoading.value[key]
    } else if (numKey > index) {
      newLoadingState[numKey - 1] = productLoading.value[key]
    }
  })
  productLoading.value = newLoadingState
}

// 商品选择变化
const handleProductChange = (productId, index) => {
  const product = productList.value.find(p => p.id === productId)
  if (product) {
    form.value.items[index].price = product.price
    calculateTotal()
  }
}

// 计算总金额
const calculateTotal = () => {
  form.value.total_price = calculateTotalAmount()
}

// 计算总金额的具体方法
const calculateTotalAmount = () => {
  return form.value.items.reduce((total, item) => {
    return total + (item.price * (item.quantity || 0))
  }, 0).toFixed(2)
}

// 获取商品列表
const fetchProductList = async () => {
  try {
    const response = await getProductList({ page: 1, pageSize: 100 })
    console.log('商品列表数据:', response)
    productList.value = response.data || []
    searchProductList.value = [...productList.value] // 保存原始商品列表
  } catch (error) {
    console.error('获取商品列表失败:', error)
    ElMessage.error('获取商品列表失败')
    productList.value = []
  }
}

// 获取用户列表
const fetchUserList = async () => {
  try {
    const response = await getSimpleUserList()
    userList.value = response || []
  } catch (error) {
    console.error('获取用户列表失败:', error)
    ElMessage.error('获取用户列表失败')
    userList.value = []
  }
}

const remoteSearchUser = async (query) => {
  if (query.trim() === '') {
    // 如果查询为空，加载所有用户
    await fetchUserList()
    return
  }
  
  userLoading.value = true
  try {
    // 调用API搜索用户，假设后端有搜索接口
    // 如果没有专门的搜索接口，可以先获取所有用户再进行前端过滤
    const response = await getSimpleUserList({ page: 1, pageSize: 20, search: query })
    const filteredUsers = response.filter(user => 
      user.name.toLowerCase().includes(query.toLowerCase())
    )
    userList.value = filteredUsers
  } catch (error) {
    console.error('搜索用户失败:', error)
    ElMessage.error('搜索用户失败')
  } finally {
    userLoading.value = false
  }
}

// 远程搜索商品
const remoteSearchProduct = async (query, index) => {
  // 设置当前索引的加载状态
  productLoading.value[index] = true
  
  try {
    // 如果查询为空，使用原始商品列表
    if (query.trim() === '') {
      productList.value = [...searchProductList.value]
      productLoading.value[index] = false
      return
    }
    
    // 调用API搜索商品
    const response = await getProductList({ page: 1, pageSize: 20, search: query })
    productList.value = response.data || []
  } catch (error) {
    console.error('搜索商品失败:', error)
    ElMessage.error('搜索商品失败')
  } finally {
    productLoading.value[index] = false
  }
}

// 监听表单数据变化
watch(() => props.formData, (newVal) => {
  if (newVal && Object.keys(newVal).length > 0) {
    form.value = {
      ...newVal,
      user_id: newVal.user_id || null,
      items: newVal.items?.map(item => ({
        product_id: item.product_id,
        quantity: item.quantity || 1,
        price: item.price || 0
      })) || []
    }
  } else {
    form.value = {
      user_id: null,
      status: 'pending',
      items: [],
      remark: '',
      total_price: 0
    }
  }
}, { deep: true, immediate: true })

// 提交表单
const submit = async () => {
  await formRef.value?.validate()
  try {
    if (form.value.id) {
      await updateOrder(form.value.id, form.value)
      ElMessage.success('更新成功')
    } else {
      await createOrder(form.value)
      ElMessage.success('创建成功')
    }
    emit('submit')
  } catch (error) {
    console.error('保存订单失败:', error)
  }
}

defineExpose({
  submit
})

onMounted(() => {
  fetchProductList()
  fetchUserList()
})

const rules = {
  user_id: [
    { required: true, message: '请选择用户', trigger: 'change' }
  ],
  items: [
    { 
      required: true, 
      message: '请至少添加一个商品', 
      trigger: 'change',
      validator: (rule, value, callback) => {
        if (!value || value.length === 0) {
          callback(new Error('请至少添加一个商品'))
        } else {
          // 检查每个商品是否都选择了商品和数量
          const invalidItem = value.find(item => !item.product_id || !item.quantity)
          if (invalidItem) {
            callback(new Error('请完善商品信息'))
          } else {
            callback()
          }
        }
      }
    }
  ],
  status: [
    { required: true, message: '请选择订单状态', trigger: 'change' }
  ]
}
</script>

